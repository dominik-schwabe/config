vim.keymap.set({ "n" }, ":", function ()
  if not vim.api.nvim_buf_get_name(0):match("%[Command Line%]$") then
    U.feedkeys("<ESC>q:", "n", true)
  else
    U.feedkeys("<ESC>:", "n", true)
  end
end)
